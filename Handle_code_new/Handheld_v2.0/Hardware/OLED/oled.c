/********************************************************
* 名称:OLED_DrawLine()
      采用布兰森汉姆(Bresenham)算法画线,根据硬件特点实现加速
* 功能:任意两点间的直线，对于水平和竖直的直线，可以优化加速
* 入口参数:x0     直线起点所在行的位置
*         	y0     直线起点所在列的位置
*         	x1     直线终点所在行的位置
'       	y1     直线终点所在列的位置
* 出口参数:  无
* 说明:操作失败的原因是指定地址超出缓冲区域，任意是指行和列的相对大小没有限制，绝对大小手屏幕限制
*********************************************************/
void OLED_DrawLine(uint8_t x0,uint16_t y0,uint8_t x1,uint16_t y1,uint16_t color)
{
	int temp;
	int dx,dy;               //定义起点到终点的横纵坐标的增加值
	int s1,s2,status,i;
	int Dx,Dy,sub;
	dx=x1-x0;
	if(dx>=0)                 //X的方向是增加的
	  s1=1;
	else                     //X的方向是降低的
	  s1=-1;     
	dy=y1-y0;                 //判断Y的方向是增加还是降低的
	if(dy>=0)
	  s2=1;
	else
	  s2=-1;
  
	Dx=s1*dx;               //计算横纵标志增加值的绝对值
	Dy=s2*dy;
	if(Dy>Dx)                               
	  {                     //以45度角为分界线,靠近Y是status=1,靠近X轴是status=0 
	  temp=Dx;
	  Dx=Dy;
	  Dy=temp;
	  status=1;
	  } 
	else
	  status=0;

	/********判断垂直和水平线*******/	 //硬件加速部分
	if(dx==0)                   //横向上没有增量，画一条水平线，更具硬件特点实现加速
	   
	   //OLED_DrawVerticalLine(x0,y0,y0,color);
	   //OLED_DrawVerticalLine(x0,320,120,0x07E0);
	   //OLED_XYLine(x0,y0,x1,y1,color);
	if(dy==0)                   //纵向上没有增量，画一条垂直线
	   OLED_DrawLevelLine(x0,x1,y0,color);
	   //OLED_XYLine(x0,y0,x1,y1,color);
	/*********Bresenham算法画任意两点间直线********/ 
	sub=2*Dy-Dx;                 //第一次判断下个点的位置
	for(i=0;i<Dx;i++)
	{          
		OLED_CursorXY(x0,y0);
		WriteData(color); 
	    if(sub>=0)                               
	    { 
	    if(status==1)               //在靠近Y轴区,x值加1
	      	x0+=s1; 
	    else                     //在靠近x轴区,y值加1             
	      	y0+=s2; 
	    sub-=2*Dx;                 //判断下个点的位置
	    } 
	    if(status==1)
	  		y0+=s2; 
	    else       
	    	x0+=s1; 
	    sub+=2*Dy; 	        
	} 
}

/*****************************************
名称:OLED_DrawCircle (在任意位置画圆)
说明:使用Bresenham法画1/8个圆,再利用对称性画出其他的7/8个圆 
    

    按下图吧圆分为8份
        0
      7    1
    6        2
      5    3
        4 
*****************************************/
void OLED_DrawCircle(uint8_t x0,uint16_t y0,uint8_t r,uint16_t color)
{
	int a,b;
	int di;
	a=0;
	b=r;
	di=3-2*r;             //判断下个点位置的标志
	while(a<=b)
	{
	OLED_CursorXY(x0-b,y0-a);WriteData(color);	 	 //3
	OLED_CursorXY(x0+b,y0-a);WriteData(color);	 	 //0
	OLED_CursorXY(x0-a,y0+b);WriteData(color);		 //1
	OLED_CursorXY(x0-b,y0-a);WriteData(color);		 //7
	OLED_CursorXY(x0-a,y0-b);WriteData(color);		 //2
	OLED_CursorXY(x0+b,y0+a);WriteData(color);		 //4
	OLED_CursorXY(x0+a,y0-b);WriteData(color);		 //5
	OLED_CursorXY(x0+a,y0+b);WriteData(color);		 //6
	OLED_CursorXY(x0-b,y0+a);WriteData(color);		 //7
	  a++;
	  /***使用Bresenham算法画圆**/     
	  if(di<0)
	  di +=4*a+6;
	  else
	  {
	    di +=10+4*(a-b);   
	    b--;
	  } 
	  OLED_CursorXY(x0+a,y0+b);WriteData(color);		 
	  }
}

