//*********************OLED写命令函数************************//
void WriteCommand(unsigned int Data)
{
	unsigned int Command;	
	Command=Data;
	
	OLED_RS_Clr();		   
	OLED_CS_Clr();
	OLED_WRD_Clr();
	uDelay(2);
	
	DATAOUT(Command);
	uDelay(2);
	
	OLED_WRD_Set();
	OLED_CS_Set();	  
	OLED_RS_Set();
}
//*********************OLED写数据函数16位并行数据接口************************//
void WriteData(unsigned int Data)
{
	unsigned int xDATA;	
	xDATA=Data;
	
	OLED_RS_Set();   
	OLED_CS_Clr();
	OLED_WRD_Clr();
	uDelay(2);
	
	DATAOUT(xDATA);
	uDelay(2);
	
	OLED_WRD_Set();
	OLED_CS_Set();	  
	OLED_RS_Set();
	
}
