void OLED_DisplayON(uint8_t choice)	  //开关屏幕的显示，0关闭，1或其他打开
{
	if(choice)
	{
		WriteCommand(0x0005);
		WriteData(0x0001);     // Display On  
	}
	else
	{
	  	WriteCommand(0x0005);
		  WriteData(0x0000);  	//Display Off 
	}

}
void OLED_Clear(void)				  //清屏函数
{
	int m,n;
	OLED_DisplayON(0);			  //Display Off
	OLED_CursorXY(0,0);			  //定位开始处（x，y）=（0.0）
	for(m=0;m<320;m++)
	{
		for(n=0;n<240;n++)
		{
			//WriteData(0x07e0);	  //写入0，全部清空，显示黑色
			WriteData(0X0000);
		}
	}
	OLED_DisplayON(1);			  //Display On 
}
